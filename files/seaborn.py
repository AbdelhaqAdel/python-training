import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

flights = sns.load_dataset("flights")
flights.head(5)

x_feb = flights[flights['month']=='Feb']
x_feb

sns.set_style("darkgrid")
sns.lineplot(x="year",y="passengers",data= x_feb)

sns.set_style("darkgrid")
sns.lineplot(x="year",y="passengers",data= x_feb)
plt.title("Year vs. ")
plt.xlabel("Year")
plt.ylabel("Passengers")

sns.set_style("darkgrid")
sns.lineplot(x="year",y="passengers",data= flights,hue='month')
plt.title("Year vs. ")
plt.xlabel("Year")
plt.ylabel("Passengers")

sns.set_style("darkgrid")
sns.lineplot(x="year",y="passengers",data= flights,style='month')
plt.title("Year vs. ")
plt.xlabel("Year")
plt.ylabel("Passengers")

plt.figure(figsize=(5,5))
sns.set_style("darkgrid")
