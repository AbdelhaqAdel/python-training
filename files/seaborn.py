fmri = sns.load_dataset("fmri")
fmri.head(5)

plt.figure(figsize=(5,5))
sns.set_style("ticks")
sns.lineplot(data= fmri,x="timepoint",y="signal",hue='event',style='region')
plt.title("Year vs. ")
plt.xlabel("Year")
plt.ylabel("Passengers")

plt.figure(figsize=(5,5))
sns.set_style("ticks")
sns.relplot(data= fmri,x="timepoint",y="signal",hue='event',style='region',kind='line',col='event',row='region')
